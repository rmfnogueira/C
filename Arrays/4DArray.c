// 4D Array
#include <stdio.h>
int main()
{
    int i, j, k, l, s;
    int d[2][2][2][2];
    s = 2;
    d[0][0][0][0] = 4;
    d[0][0][0][1] = 3;
    d[0][0][1][0] = 2;
    d[0][0][1][1] = 6;
    d[0][1][0][0] = 6;
    d[0][1][0][1] = 8;
    d[0][1][1][0] = 1;
    d[0][1][1][1] = 2;
    d[1][0][0][0] = 6;
    d[1][0][0][1] = 9;
    d[1][0][1][0] = 5;
    d[1][0][1][1] = 1;
    d[1][1][0][0] = 9;
    d[1][1][0][1] = 7;
    d[1][1][1][0] = 5;
    d[1][1][1][1] = 7;
    for (i = 0; i < s; i++)
    {
        for (j = 0; j < s; j++)
        {
            for (k = 0; k < s; k++)
            {
                for (l = 0; l < s; l++)
                {
                    printf("Value of stdio[%d][%d][%d][%d]: %d ", i, j, k, l, d[i][j][k][l]);
                    printf("\n");
                }
            }
        }
    }
    return 0;
}